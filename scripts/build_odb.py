def q(v):
    """Render a Python value as a Firebird SQL literal."""
    if v is None:
        return "NULL"
    if isinstance(v, str):
        return "'" + v.replace("'", "''") + "'"
    return str(v)


def seed_statements():
    stmts = []
    for r in ACCOUNTS:
        stmts.append('INSERT INTO Accounts (id,name,"type",starting_balance) VALUES (%s)'
                     % ",".join(q(x) for x in r))
    for r in CATEGORIES:
        stmts.append('INSERT INTO Categories (id,name,parent_category_id,kind) VALUES (%s)'
                     % ",".join(q(x) for x in r))
    for r in TRANSACTIONS:
        stmts.append('INSERT INTO Transactions (id,"date",account_id,category_id,amount,description,"type") VALUES (%s)'
                     % ",".join(q(x) for x in r))
    for r in BUDGETS:
        stmts.append('INSERT INTO Budgets (id,category_id,"month",planned_amount) VALUES (%s)'
                     % ",".join(q(x) for x in r))
    for r in RECURRING:
        stmts.append('INSERT INTO RecurringTransactions (id,description,amount,frequency,category_id,account_id,next_due_date) VALUES (%s)'
                     % ",".join(q(x) for x in r))
    # Advance IDENTITY sequences past the explicit ids we inserted.
    stmts += [
        "ALTER TABLE Accounts ALTER COLUMN id RESTART WITH %d" % (len(ACCOUNTS) + 1),
        "ALTER TABLE Categories ALTER COLUMN id RESTART WITH %d" % (len(CATEGORIES) + 1),
        "ALTER TABLE Transactions ALTER COLUMN id RESTART WITH %d" % (len(TRANSACTIONS) + 1),
        "ALTER TABLE Budgets ALTER COLUMN id RESTART WITH %d" % (len(BUDGETS) + 1),
        "ALTER TABLE RecurringTransactions ALTER COLUMN id RESTART WITH %d" % (len(RECURRING) + 1),
    ]
    return stmts


# --- UNO plumbing ------------------------------------------------------------

SOFFICE = r"C:\Program Files\LibreOffice\program\soffice.bin"
PORT = 2103
PROFILE = "file:///c:/temp/lo_odb_build"


def connect(ctx_local):
    import uno
    resolver = ctx_local.ServiceManager.createInstanceWithContext(
        "com.sun.star.bridge.UnoUrlResolver", ctx_local)
    url = ("uno:socket,host=localhost,port=%d;urp;StarOffice.ComponentContext" % PORT)
    last = None
    for _ in range(60):
        try:
            return resolver.resolve(url)
        except Exception as e:  # NoConnectException until soffice is ready
            last = e
            time.sleep(0.5)
    raise RuntimeError("Could not connect to soffice: %s" % last)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--odb", default=os.path.join(
        os.path.dirname(os.path.dirname(os.path.abspath(__file__))), "budget.odb"))
    ap.add_argument("--keep", action="store_true",
                    help="abort if the .odb already exists instead of overwriting")
    args = ap.parse_args()

    import uno
    from com.sun.star.beans import PropertyValue  # noqa: F401 (validates bridge)

    odb_path = os.path.abspath(args.odb)
    if os.path.exists(odb_path):
        if args.keep:
            print("Refusing to overwrite existing %s (--keep)" % odb_path)
            return 1
        os.remove(odb_path)
    odb_url = uno.systemPathToFileUrl(odb_path)

    log("spawning soffice on port %d" % PORT)
    proc = subprocess.Popen([
        SOFFICE, "--headless", "--norestore", "--invisible", "--nologo",
        "--nofirststartwizard", "-env:UserInstallation=" + PROFILE,
        "--accept=socket,host=localhost,port=%d;urp;StarOffice.ServiceManager" % PORT,
    ])
    try:
        local = uno.getComponentContext()
        log("connecting...")
        ctx = connect(local)
        log("connected")
        smgr = ctx.ServiceManager

        db_context = smgr.createInstanceWithContext(
            "com.sun.star.sdb.DatabaseContext", ctx)
        data_source = db_context.createInstance()
        data_source.URL = "sdbc:embedded:firebird"
        db_doc = data_source.DatabaseDocument
        log("storeAsURL %s" % odb_url)
        db_doc.storeAsURL(odb_url, ())

        log("getConnection (embedded firebird)...")
        conn = data_source.getConnection("", "")
        log("got connection; running DDL")
        stmt = conn.createStatement()

        for sql in DDL:
            stmt.execute(sql)
        log("DDL done; seeding")
        for sql in seed_statements():
            stmt.execute(sql)
        log("seed done; storing")

        # Persist the embedded Firebird database back into the .odb package.
        db_doc.store()
        conn.close()
        db_doc.close(False)
        log("stored and closed")
        print("Created %s" % odb_path)
        return 0
    finally:
        try:
            desktop = smgr.createInstanceWithContext(
                "com.sun.star.frame.Desktop", ctx)
            desktop.terminate()
        except Exception:
            pass
        try:
            proc.wait(timeout=20)
        except Exception:
            proc.terminate()


if __name__ == "__main__":
    sys.exit(main())
