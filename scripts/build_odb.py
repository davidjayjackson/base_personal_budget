#!/usr/bin/env python
"""
build_odb.py - Reproducibly generate budget.odb (LibreOffice Base, embedded Firebird)
from the schema designed in personal_budget.ods.

Creates 5 tables (Accounts, Categories, Transactions, Budgets,
RecurringTransactions) with primary keys, foreign keys and CHECK constraints,
then seeds the relationally-complete reference data (all accounts + all
categories + the sample transactions/budgets/recurring rows).

Run with LibreOffice's bundled Python (it has the `uno` module):

    "C:\\Program Files\\LibreOffice\\program\\python.exe" scripts\\build_odb.py

The script spawns its own headless soffice on a private socket + user profile,
so it does not interfere with an interactive LibreOffice session.

Re-running overwrites budget.odb (pass --keep to abort if it already exists).
"""
import os
