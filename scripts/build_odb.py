def log(msg):
    sys.stderr.write("[build_odb] %s\n" % msg)
    sys.stderr.flush()

# --- Reserved SQL words are quoted:  "date", "type", "month" -----------------

DDL = [
    """CREATE TABLE Accounts (
        id               INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
        name             VARCHAR(50) NOT NULL,
        "type"           VARCHAR(20) NOT NULL,
        starting_balance DECIMAL(12,2) DEFAULT 0.00 NOT NULL,
        CONSTRAINT chk_acct_type CHECK ("type" IN ('Checking','Savings','Credit','Investment'))
    )""",
    """CREATE TABLE Categories (
        id                 INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
        name               VARCHAR(50) NOT NULL,
        parent_category_id INTEGER,
        kind               VARCHAR(10),
        CONSTRAINT fk_cat_parent FOREIGN KEY (parent_category_id) REFERENCES Categories(id),
        CONSTRAINT chk_cat_kind  CHECK (kind IN ('Income','Expense'))
    )""",
    """CREATE TABLE Transactions (
        id          INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
        "date"      DATE NOT NULL,
        account_id  INTEGER,
        category_id INTEGER,
        amount      DECIMAL(12,2) NOT NULL,
        description VARCHAR(255),
        "type"      VARCHAR(10),
        CONSTRAINT fk_txn_acct FOREIGN KEY (account_id)  REFERENCES Accounts(id),
        CONSTRAINT fk_txn_cat  FOREIGN KEY (category_id) REFERENCES Categories(id),
        CONSTRAINT chk_txn_type CHECK ("type" IN ('Income','Expense'))
    )""",
    """CREATE TABLE Budgets (
        id             INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
        category_id    INTEGER,
        "month"        DATE NOT NULL,
        planned_amount DECIMAL(12,2) NOT NULL,
        CONSTRAINT fk_bud_cat FOREIGN KEY (category_id) REFERENCES Categories(id),
        CONSTRAINT uq_bud_cat_month UNIQUE (category_id, "month")
    )""",
    """CREATE TABLE RecurringTransactions (
        id            INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
        description   VARCHAR(255) NOT NULL,
        amount        DECIMAL(12,2) NOT NULL,
        frequency     VARCHAR(20) NOT NULL,
        category_id   INTEGER,
        account_id    INTEGER,
        next_due_date DATE NOT NULL,
        CONSTRAINT fk_rec_cat  FOREIGN KEY (category_id) REFERENCES Categories(id),
        CONSTRAINT fk_rec_acct FOREIGN KEY (account_id) REFERENCES Accounts(id),
        CONSTRAINT chk_rec_freq CHECK (frequency IN ('Daily','Weekly','Monthly','Yearly'))
    )""",
]

